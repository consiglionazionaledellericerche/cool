package it.cnr.cool.exception;

public class MockMvcException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4767363554991552976L;

	public MockMvcException() {
	}

	public MockMvcException(String arg0) {
		super(arg0);
	}

	public MockMvcException(Throwable arg0) {
		super(arg0);
	}

	public MockMvcException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
